# Example 6: Dictionary Keys, Values, and Items
# We can retrieve all keys, values, or key-value pairs using the keys(), values(), and items() methods, respectively.

# Example dictionary
my_dict = {'name': 'John', 'age': 25, 'city': 'New York'}

# Retrieving keys, values, and items
keys = my_dict.keys()
values = my_dict.values()
items = my_dict.items()

# Printing keys, values, and items
print('Keys:',keys)
print('Values:', values)
print('Items:',items)
